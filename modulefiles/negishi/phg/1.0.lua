help([==[

Description
===========
Practical Haplotype Graph (PHG) is a general, graph-based, computational framework that can be used with a variety of skim sequencing methods to infer high-density genotypes directly from low-coverage sequence.

More information
================
 - Docker hub: https://hub.docker.com/r/maizegenetics/phg
 - Home page: https://www.maizegenetics.net/phg    
]==])

whatis("Name: Phg")
whatis("Version: 1.0")
whatis("Description: Practical Haplotype Graph (PHG) is a general, graph-based, computational framework that can be used with a variety of skim sequencing methods to infer high-density genotypes directly from low-coverage sequence.")
whatis("Docker hub: https://hub.docker.com/r/maizegenetics/phg")
whatis("Home page:     https://www.maizegenetics.net/phg")

if not (os.getenv("BIOC_SINGULARITY_MODULE") == "none") then
   local singularity_module = os.getenv("BIOC_SINGULARITY_MODULE") or "Singularity"
   if not (isloaded(singularity_module)) then
      load(singularity_module)
   end
end

conflict(myModuleName())

--       Think executables, mpirun, possibly Perl or Python, etc.
local image = "phg_1.0.sif"
local uri = "docker://maizegenetics/phg:1.0"
local programs = {"CreateConsensi.sh", "CreateHaplotypes.sh", "CreateReferenceIntervals.sh", "CreateSmallDataSet.sh", "CreateValidIntervalsFile.sh", "IndexPangenome.sh", "LoadAssemblyAnchors.sh", "LoadGenomeIntervals.sh", "ParallelAssemblyAnchorsLoad.sh", "RunLiquibaseUpdates.sh", "CreateHaplotypesFromBAM.groovy", "CreateHaplotypesFromFastq.groovy", "CreateHaplotypesFromGVCF.groovy"}
local entrypoint_args = ""

-- The absolute path to Singularity is needed so it can be invoked on remote
-- nodes without the corresponding module necessarily being loaded.
-- Trim off the training newline.
local singularity = capture("which singularity | head -c -1")

if (os.getenv("BIOC_IMAGE_DIR")) then
   image = pathJoin(os.getenv("BIOC_IMAGE_DIR"), image)

   if not (isFile(image)) then
      -- The image could not be found in the container directory
      if (mode() == "load") then
         LmodMessage("file not found: " .. image)
         LmodMessage("The container image will be pulled upon first use to the Singularity cache")
      end
      image = uri

      -- Alternatively, this could pull the container image and
      -- save it in the container directory
      --if (mode() == "load") then
      --   subprocess(singularity .. " pull " .. image .. " " .. uri)
      --end
   end
else
   -- Look for the image in the Singularity cache, and if not found
   -- download it when "singularity run" is invoked.
   image = uri
end

-- Determine Nvidia and/or AMD GPUs (to pass coresponding flag to Singularity)
local run_args = {}
if (capture("nvidia-smi -L 2>/dev/null") ~= "") then
   if (mode() == "load") then
      LmodMessage("BIOC: Enabling Nvidia GPU support in the container.")
   end
   table.insert(run_args, "--nv")
end
if (capture("/opt/rocm/bin/rocm-smi -i 2>/dev/null | grep ^GPU") ~= "") then
   if (mode() == "load") then
      LmodMessage("BIOC: Enabling AMD GPU support in the container.")
   end
   table.insert(run_args, "--rocm")
end

-- And assemble container command
local container_launch = singularity .. " exec " .. table.concat(run_args, " ") .. " " .. image .. " " .. entrypoint_args

-- Multinode support
-- setenv("OMPI_MCA_orte_launch_agent", container_launch .. " orted")

-- Programs to setup in the shell
for i,program in pairs(programs) do
    set_shell_function(program, container_launch .. " " .. program .. " \"$@\"",
                                container_launch .. " " .. program .. " $*")
end

-- Additional commands or environment variables, if any
prepend_path("SINGULARITYENV_PREPEND_PATH","/")
prepend_path("APPTAINERENV_PREPEND_PATH","/")
